class Application {
    constructor(
      application_id,
      listing_id,
      student_id,
      status,
      message,
      created_at,
      updated_at
    ) {
      this.application_id = application_id;
      this.listing_id = listing_id;
      this.student_id = student_id;
      this.status = status;
      this.message = message;
      this.created_at = created_at;
      this.updated_at = updated_at;
    }
  }
  